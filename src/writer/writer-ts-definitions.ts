import { join } from "node:path";
import { convertSvelteExt, createExports } from "../create-exports";
import { info } from "../logger";
import type { ParsedExports } from "../parse-exports";
import type { ComponentDocs } from "../plugin";
import { buildComponentApiDocument } from "./document-model";
import { createTypeScriptWriter } from "./Writer";
import { type WriteTsDefinitionOptions, writeTsDefinition } from "./writer-ts-definitions-core";

/**
 * Re-export browser-compatible functions from core module.
 *
 * These functions are exported from the core TypeScript definitions writer
 * to provide a browser-compatible API surface. The core module contains the
 * actual implementation logic.
 *
 * @see {@link ./writer-ts-definitions-core} for the core implementation
 */
export {
  formatTsProps,
  getContextDefs,
  getTypeDefs,
  type WriteTsDefinitionOptions,
  writeTsDefinition,
} from "./writer-ts-definitions-core";

/**
 * @example
 * ```ts
 * const options: WriteTsDefinitionsOptions = {
 *   outDir: "./dist",
 *   inputDir: "./src",
 *   preamble: "// Generated by sveld\n",
 *   exports: { ... }
 * };
 * ```
 */
export interface WriteTsDefinitionsOptions extends WriteTsDefinitionOptions {
  outDir: string;
  inputDir: string;
  preamble: string;
  exports: ParsedExports;
  /** Report resolved paths instead of writing. Set by `sveld --dry-run`. */
  dryRun?: boolean;
}

/**
 * @example
 * ```ts
 * await writeTsDefinitions(components, {
 *   outDir: "./dist",
 *   inputDir: "./src",
 *   preamble: "// Generated\n",
 *   exports: { ... }
 * });
 * // Creates dist/Button.d.ts, dist/Input.d.ts, dist/index.d.ts
 * ```
 */
export default async function writeTsDefinitions(components: ComponentDocs, options: WriteTsDefinitionsOptions) {
  const ts_base_path = join(process.cwd(), options.outDir, "index.d.ts");
  const writer = createTypeScriptWriter({ dryRun: options.dryRun });
  const indexDTs = options.preamble + createExports(options.exports);

  const document = buildComponentApiDocument(components);
  const writePromises = document.components.map(async (component) => {
    const ts_filepath = convertSvelteExt(join(options.outDir, component.filePath));
    await writer.write(ts_filepath, writeTsDefinition(component, { format: options.format }));
  });

  await Promise.all([...writePromises, writer.write(ts_base_path, `${indexDTs}\n`)]);

  if (!options.dryRun) info("created TypeScript definitions.");
}
